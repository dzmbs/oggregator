import type { EnrichedStrike, VenueQuote, EnrichedSide } from '@shared/enriched';

function mkQuote(overrides: Partial<VenueQuote> = {}): VenueQuote {
  return {
    bid: 120,
    ask: 130,
    mid: 125,
    bidSize: 10,
    askSize: 8,
    markIv: 0.52,
    bidIv: 0.5,
    askIv: 0.54,
    delta: 0.45,
    gamma: 0.00012,
    theta: -15.2,
    vega: 42.5,
    spreadPct: 7.7,
    totalCost: 132,
    estimatedFees: { maker: 0.8, taker: 1.2 },
    openInterest: 1200,
    volume24h: 350,
    openInterestUsd: 150000,
    volume24hUsd: 43750,
    ...overrides,
  };
}

function mkSide(
  bestVenue: 'deribit' | 'okx' | 'binance' | 'bybit' | 'derive',
  venues: Partial<Record<string, Partial<VenueQuote>>>,
  bestIv: number | null = 0.52,
): EnrichedSide {
  const built: EnrichedSide['venues'] = {};
  for (const [v, overrides] of Object.entries(venues)) {
    (built as Record<string, VenueQuote>)[v] = mkQuote(overrides);
  }
  return { venues: built, bestVenue, bestIv };
}

